            <h2 class="titulo1">Informacion Introductoria</h2>
            <div class="contenido-dietas">
                <p class="texto-info">En la sociedad actual, el sedentarismo y los hábitos alimentarios poco saludables se han convertido en problemas comunes que afectan a un gran número de personas. La falta de actividad física regular y una dieta desequilibrada han llevado a un aumento alarmante de los índices de obesidad, enfermedades cardiovasculares y otros problemas de salud relacionados.</p>
            
                <p class="texto-info">Es crucial entender los principios básicos de una alimentación saludable y cómo estructurar una rutina de ejercicios efectiva. Algunos consejos para mantener un estilo de vida saludable incluyen:</p>
            
                <ul>
                  <li>Realizar actividad física regularmente, como caminar, correr o practicar deportes.</li>
                  <li>Consumir una dieta equilibrada que incluya una variedad de frutas, verduras, granos enteros, proteínas magras y grasas saludables.</li>
                  <li>Limitar la ingesta de alimentos procesados, azúcares añadidos y grasas saturadas.</li>
                  <li>Beber suficiente agua para mantenerse hidratado.</li>
                  <li>Establecer metas realistas y gradualmente aumentar la intensidad de los ejercicios.</li>
                </ul>
            
                <p class="texto-info">Si bien puede haber barreras, como la falta de tiempo y la falta de motivación, es importante recordar que pequeños cambios pueden marcar la diferencia. Adoptar un enfoque gradual y buscar apoyo de profesionales de la salud o grupos de apoyo pueden ser beneficiosos en el camino hacia un estilo de vida más saludable.</p>
            
